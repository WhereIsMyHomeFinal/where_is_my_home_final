package com.ssafy.homeapp.model.dto;

public class Notice {

	private int id;
	private String userid, title, content;

	public Notice(int id, String title, String userid, String content) {
		super();
		this.id = id;
		this.userid = userid;
		this.title = title;
		this.content = content;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
}
